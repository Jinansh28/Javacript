const month =3

switch(month){
    case 1: 
    console.log("January");
    break;
    case 2: 
    console.log("Febuary");
    break;
    case "mar": 
    console.log("March");
    break;
    case 4: 
    console.log("April");
    break;
    case 5: 
    console.log("May");
    break;
    default:
    console.log("June");
    break;
}
// Can use any datatype to compare cases

